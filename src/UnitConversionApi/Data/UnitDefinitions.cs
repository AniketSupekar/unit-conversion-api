    public static IReadOnlyDictionary<string, IEnumerable<string>> GetUnitsByCategory()
    {
        return Units
            .GroupBy(kvp => kvp.Value.Category)
            .ToDictionary(
                g => g.Key,
                g => g.Select(kvp => kvp.Key).Distinct()
            );
    }
